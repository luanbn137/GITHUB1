   private void inIT(){
       this.Index = 1;
       this.updateStatus();
       this.loadtblHD();
       this.loadToCbxSIZE();
       AutoCompleteDecorator.decorate(this.cbbSize);
       this.loadToCbxMAUSAC();
       AutoCompleteDecorator.decorate(this.cbbMauSac);
       this.loadToCbxChatLieu();
       AutoCompleteDecorator.decorate(this.cbbChatLieu);
       this.loadToCbxLoplot();
       AutoCompleteDecorator.decorate(this.cbbloplot);
   }
    private void updateStatus() {
        boolean edit = (this.Index >= 0);
        boolean first = (this.Index == 0);
        boolean last = (this.Index == this.tblSanPham.getRowCount() - 1);
        this.txtTenSanPhamCT.setEditable(!edit);
        this.btnThem.setEnabled(!edit);
        this.btnSua.setEnabled(edit);
        this.btnxoa.setEnabled(edit);
        this.btndau.setEnabled(edit && !first);
        this.btnnext.setEnabled(edit && !first);
        this.btnnext.setEnabled(edit && !last);
        this.btncuoi.setEnabled(edit && !last);
    }
   
    private SanPhamChiTiet getModel(){
        SanPhamChiTiet entity = new SanPhamChiTiet();
        entity.setIdSanPham(Integer.parseInt( this.txtMaSanPham.getText()));
        entity.setTenSP(this.txtTenSanPhamCT.getText());
        String soluong = this.tfSoLuong.getText();
        entity.setSoLuong(Integer.parseInt(soluong));
        entity.setGia(Double.parseDouble(this.tfGia.getText()));
        entity.setMoTa(this.taMoTa.getText());
        entity.setIdSize(Integer.parseInt(this.cbbSize.getSelectedItem() + ""));
        entity.setIdMauSac(Integer.parseInt(this.cbbMauSac.getSelectedItem() + ""));
        entity.setIdChatLieu(Integer.parseInt(this.cbbChatLieu.getSelectedItem() + ""));
        entity.setIdLopLot(Integer.parseInt(this.cbbloplot.getSelectedItem() + ""));
        return entity;
                    
    }

        private void loadtblHD() {
            this.dtm.setColumnCount(0);
            this.dtm.setRowCount(0);
         this.dtm.setColumnIdentifiers(new Object[]{
        "ID Sản Phẩm", "Tên Sản Phẩm", "Giá","Màu Sắc", "Chất Liệu", "Lớp Lót", "Số Lượng", " Size", "Mô Tả", "Trạng Thái"
            });
        
        try {
            this.spct = this.sPCTRepository.selectAll();
            for(SanPhamChiTiet spct_entity : this.spct){
                this.dtm.addRow(new Object[]{
                    spct_entity.getIdSanPham(),
                    spct_entity.getTenSP(),
                    spct_entity.getGia(),
                    spct_entity.getTenmausac(), 
                    spct_entity.getTenchatlieu(),
                    spct_entity.getTenloplot(), 
                    spct_entity.getSoLuong(),
                    spct_entity.getTensize(),
                    spct_entity.getMoTa(),
                    spct_entity.isTrangThai()
                });
            }
        } catch (Exception e) {
             e.printStackTrace();
        }
    }

    private void loadToCbxSIZE() {
        LSHT sizecbb = new LSHT();
        List<Size> lst = sizecbb.getAllSize();
        this.cbbSize.removeAllItems();
        for(Size size : lst){
            this.cbbSize.addItem(size.getTenSize());
        }
    }
    private void loadToCbxMAUSAC() {
        LSHT MauSaccbb = new LSHT();
        List<MauSac> lst = MauSaccbb.getAllMauSac();
        this.cbbSize.removeAllItems();
        for(MauSac mauSac : lst){
            this.cbbSize.addItem(mauSac.getTenmausac());
        }
    }
    
    private void loadToCbxChatLieu() {
        LSHT chatlieucbb = new LSHT();
        List<ChatLieu> lst = chatlieucbb.getAllChatLieuHD();
        this.cbbSize.removeAllItems();
        for(ChatLieu chatLieu : lst){
            this.cbbSize.addItem(chatLieu.getTenchatlieu());
        }
    }
    private void loadToCbxLoplot() {
        LSHT loplotcbb = new LSHT();
        List<LopLot> lst = loplotcbb.getAllLopLot();
        this.cbbSize.removeAllItems();
        for(LopLot lopLot : lst){
            this.cbbSize.addItem(lopLot.getTenloplot());
        }
    }

    private void edit() {
        if(this.Index >= 0){
            try {
                String masp = this.tblSanPhamChiTiet.getValueAt(this.Index,0).toString();
                SanPhamChiTiet entity = this.sPCTRepository.selectByID(masp);
                this.setModel(entity);
                this.updateStatus();
            } catch (Exception e) {
            }
        }
    }

    private void setModel(SanPhamChiTiet entity) {
        this.tfMaSpct.setText(Integer.toString(entity.getIdSPCT()));
        this.txtTenSanPhamCT.setText(entity.getTenSP());
        this.tfSoLuong.setText(Integer.toString(entity.getSoLuong()));
        this.tfGia.setText(Double.toString(entity.getGia()));
        this.taMoTa.setText(entity.getMoTa());
        this.cbbSize.setSelectedItem(entity.getTensize());
        this.cbbMauSac.setSelectedItem(entity.getTenmausac());
        this.cbbChatLieu.setSelectedItem(entity.getTenchatlieu());
        this.cbbloplot.setSelectedItem(entity.getTenloplot());
        
    }
   
