    private void fillDataSanPhamCT(List<SanPhamChiTiet> listspct){
        dtm.setRowCount(0);
        int index = 0;
        for (SanPhamChiTiet spct : listspct) {
            dtm.addRow(new Object[]{
                index ++,
                spct.getIdSanPham(),
                spct.getTenSP(),
                spct.getIdMauSac(),
                spct.getIdChatLieu(),
                spct.getIdLopLot(),
                spct.getSoLuong(),
                spct.getIdSize(),
                spct.getGia(),
                spct.getMoTa(),
                spct.isTrangThai()
            });
        }
    }
